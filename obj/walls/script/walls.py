maps_obj = []

def create_lvl(index):
    global maps_obj
    maps_obj = []
    
    tile_size = 32
    current_x = 0
    current_y = 0
    
    row_width = 22
    count = 0

    for num in LEVELES[index]:
        if num == 1:
            maps_obj.append(WALLS(current_x, current_y, 0))
        
        current_x += tile_size
        count += 1
        
        if count == row_width:
            current_x = 0
            current_y += tile_size
            count = 0
            
    return maps_obj